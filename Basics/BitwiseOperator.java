public class BitwiseOperator {
    public static void main(String[] args) {
        int a =6; 
        int b =10;

        //Bitwise AND
        System.out.println("\n"+(a&b));

        //Bitwise OR
        System.out.println("\n"+(a|b));

        //Bitwise XOR
        System.out.println("\n"+(a^b));

        //Bitwise left shift operator
        System.out.println("\n"+(10<<2));

        //Bitwise right shift operator
        System.out.println("\n"+(10>>1));
    }
}
